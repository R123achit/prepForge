// Score utilities
export const getScoreColor = (score) => {
  if (score >= 80) return 'text-green-500'
  if (score >= 60) return 'text-yellow-500'
  return 'text-red-500'
}

export const getScoreBadge = (score) => {
  if (score >= 90) return { text: 'Excellent', color: 'bg-green-500' }
  if (score >= 80) return { text: 'Good', color: 'bg-blue-500' }
  if (score >= 60) return { text: 'Average', color: 'bg-yellow-500' }
  return { text: 'Needs Improvement', color: 'bg-red-500' }
}

